var students = ["Blake", "Vicky", "Harry", "Ty"];
var temperature = [34, 42, 55, 28];
var cold = [true, true, false, true];
// pop will remove the last element in my students array
students.pop()
console.log(students)
// shift will remove first element in my temperature array
temperature.shift()
console.log(temperature)
// push will add another false element to my cold array
cold.push(false)
console.log(cold)

// index position = 0 and is the first element in each array always